#Exemplos de metodos comuns de utilizar nas strings
nome_do_curso = 'Tecnologia da informacao'
print(nome_do_curso.upper())
#upper deixa maiusculo
print(nome_do_curso.lower())
#lower deixa minusculo 
print(nome_do_curso.strip())
#remove os espaços como quando coloca senha e um espaço no final, o strip remove automaticamente somente a esquerda
print(nome_do_curso.rstrip())
#mesma função da strip mas no lado direito
print(nome_do_curso.find('forma'))
